# Command-line Interface
# -------------------------------------------------------
if __name__ == "__main__":
    import sys
    path = sys.argv[1]

    if not path.endswith(".pul"):
        raise Exception(f'"{path}" is not a .pul file. Pulse only reads .pul, behave.')
    
    with open(path) as f:
        source = f.read()

    lexer = Lexer(source)
    tokens = lexer.scan_tokens()

    for token in tokens:
        print(token)